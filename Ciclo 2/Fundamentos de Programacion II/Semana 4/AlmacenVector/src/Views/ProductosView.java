    private void btnGuardarFormProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFormProductosActionPerformed
        int referenciasProductos = Integer.parseInt(referenciaProductosField.getText());
        String nombresProductos = nombreProductosField.getText();
        int cantidadProductos = Integer.parseInt(cantidadProductosField.getText());
        PrincipalSoft.registrarProductos(referenciasProductos, nombresProductos, cantidadProductos);
        
        //Luego de registrar valores en el objeto y dejar los textfield en blanco
        referenciaProductosField.setText("");
        nombreProductosField.setText("");
        cantidadProductosField.setText("");
    }//GEN-LAST:event_btnGuardarFormProductosActionPerformed

