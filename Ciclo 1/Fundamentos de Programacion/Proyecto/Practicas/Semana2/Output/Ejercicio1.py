#Convertir Grados Celsius a Kelvin.

gradosCelsius = float(input("Ingrese la cantidad de grados Celsius: "))

kelvin = gradosCelsius + 273.15

print("\nLa operación es la siguiente: K = C + 273.15. \n")
print(f"Sumamos {gradosCelsius} + 273.15 = {kelvin} \n")
print(f"El resultado de la conversión es: {gradosCelsius}ºC = {kelvin}ºK. \n")

