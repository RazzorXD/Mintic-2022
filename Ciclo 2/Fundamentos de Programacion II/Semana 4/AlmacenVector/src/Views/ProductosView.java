    private void btnConsultarFormProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarFormProductosActionPerformed
        //boton consultar, lee el valor que hay en el campo consultar, y luego usa ese valor para llamar al metodo consultar en PrincipalSoft
        int referenciaProductos = Integer.parseInt(consultarProductosField.getText());
        PrincipalSoft.consultarProductos(referenciaProductos);
    }//GEN-LAST:event_btnConsultarFormProductosActionPerformed
    public void imprimirProductos(Productos productos){
        //Recibe como parametro el vector y cada uno de los campos se los asigna a los field correspondientes
        this.referenciaProductosField.setText(Integer.toString(productos.getReferenciasProductos()));
        this.nombreProductosField.setText(productos.getNombresProductos());
        this.cantidadProductosField.setText(Integer.toString(productos.getCantidadProductos()));
    }
   
